df["Date"] = pd.to_datetime(df["Date"])

"""outlier

gunakan IQR
"""

numeric = df.select_dtypes(include=np.number)

for col in numeric.columns:

    Q1 = df[col].quantile(0.25)

    Q3 = df[col].quantile(0.75)

    IQR = Q3-Q1

    lower = Q1-1.5*IQR

    upper = Q3+1.5*IQR

    print(col,
          ((df[col]<lower)|(df[col]>upper)).sum())

"""Visualisasi"""

for col in numeric.columns:

    plt.figure(figsize=(6,2))

