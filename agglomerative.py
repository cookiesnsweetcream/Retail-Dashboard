"""**Feature Engineering**"""

product_df = (

    df

    .groupby(

        [

            "Store ID",

            "Category",

            "Product ID"

        ],

        as_index=False

    )

    .agg({

        "Inventory Level":"mean",

        "Units Sold":"mean",

        "Units Ordered":"mean",

        "Demand Forecast":"mean",

        "Price":"mean",

        "Discount":"mean"

    })

)

"""Inventory Turnover"""

product_df["Inventory Turnover"] = (

    product_df["Units Sold"]

    /

    (product_df["Inventory Level"]+1)

)

"""Overstock Ratio"""

product_df["Overstock Ratio"] = (

    product_df["Inventory Level"]

    /

    (product_df["Demand Forecast"]+1)

)

"""Demand Gap"""

product_df["Demand Gap"] = (

    product_df["Demand Forecast"]

    -

    product_df["Inventory Level"]

)

"""Sell Through Rate"""

product_df["Sell Through Rate"] = (

    product_df["Units Sold"]

    /

    (product_df["Units Ordered"]+1)

)

"""**Semua Fitur**"""

features = [

    "Inventory Level",

    "Units Sold",

    "Units Ordered",

    "Demand Forecast",

    "Price",

    "Discount",

    "Inventory Turnover",

    "Overstock Ratio",

    "Demand Gap",

    "Sell Through Rate"

]

"""**Correlation Analysis**"""

import matplotlib.pyplot as plt
import seaborn as sns

plt.figure(figsize=(10,8))

corr = product_df[features].corr()

sns.heatmap(
    corr,
    annot=True,
    cmap="coolwarm",
    fmt=".2f"
)

plt.show()

"""**Menghapus fitur dengan korelasi > 0.90**"""

import numpy as np

corr_matrix = product_df[features].corr().abs()

upper = corr_matrix.where(
    np.triu(
        np.ones(corr_matrix.shape),
        k=1
    ).astype(bool)
)

drop_features = [
    column
    for column in upper.columns
    if any(upper[column] > 0.90)
]

print("Fitur yang dihapus :")
print(drop_features)

"""**Update Feature**"""

features = [
    feature
    for feature in features
    if feature not in drop_features
]

print(features)

"""**Variance Threshold**"""

from sklearn.feature_selection import VarianceThreshold

selector = VarianceThreshold(
    threshold=0.01
)

X = product_df[features]

selector.fit(X)

selected = X.columns[
    selector.get_support()
]

features = list(selected)

print("Fitur setelah Variance Threshold")
print(features)

"""**Standardisasi**"""

from sklearn.preprocessing import StandardScaler

scaler = StandardScaler()

X_scaled = scaler.fit_transform(
    product_df[features]
)

"""**PCA Loading**"""

pca = PCA()

pca.fit(X_scaled)

"""Loading Matrix"""

loading = pd.DataFrame(

    pca.components_.T,

    index=features,

    columns=[
        f"PC{i+1}"
        for i in range(len(features))
    ]

)

loading

"""Hitung Importance"""

loading["Importance"] = loading.abs().sum(axis=1)

loading = loading.sort_values(
    "Importance",
    ascending=False
)

loading

"""**Pilih Top Feature PCA**"""

features = loading.head(6).index.tolist()

print(features)

"""**Sequential Forward Selection**"""

import itertools

from sklearn.cluster import AgglomerativeClustering

from sklearn.metrics import silhouette_score

results = []

"""Loop seluruh kombinasi fitur"""

for r in range(2, len(features)+1):

    for subset in itertools.combinations(features, r):

        subset = list(subset)

        scaler = StandardScaler()

        X_scaled = scaler.fit_transform(
            product_df[subset]
        )

        model = AgglomerativeClustering(

            n_clusters=3,

            linkage="ward"

        )

        labels = model.fit_predict(
            X_scaled
        )

        sil = silhouette_score(
            X_scaled,
            labels
        )

        results.append([

            subset,

            sil

        ])

"""**Ranking**"""

result_df = pd.DataFrame(

    results,

    columns=[

        "Features",

        "Silhouette"

    ]

)

result_df = result_df.sort_values(

    "Silhouette",

    ascending=False

)

result_df.head(20)

"""**Ambil Kombinasi Terbaik**"""

best_features = list(
    result_df.iloc[0]["Features"]
)

print(best_features)

"""**DEndogram**"""

plt.figure(figsize=(12,6))

linkage_matrix = linkage(
    X_scaled,
    method="ward"
)

dendrogram(
    linkage_matrix,
    truncate_mode="level",
    p=5
)

plt.title("Dendrogram")

plt.show()

"""**Clustering Final**"""

scaler = StandardScaler()

X_scaled = scaler.fit_transform(
    product_df[best_features]
)

model = AgglomerativeClustering(

    n_clusters=2,

    linkage="ward"

)

labels = model.fit_predict(
    X_scaled
)

product_df["Cluster"] = labels

"""**Evaluasi**"""

from sklearn.metrics import (

    silhouette_score,

    davies_bouldin_score,

    calinski_harabasz_score

)

print("Silhouette Score :",

      round(

          silhouette_score(

              X_scaled,

              labels

          ),

          4

      ))

print("Davies Bouldin :",

      round(

          davies_bouldin_score(

              X_scaled,

              labels

          ),

          4

      ))

print("Calinski Harabasz :",

      round(

          calinski_harabasz_score(

              X_scaled,

              labels

          ),

          4

      ))

